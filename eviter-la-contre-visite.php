                <h2>Éviter la contre visite</h2>
                <h3>Voici nos conseils pour bien préparer votre contrôle technique.</h3>

                <p id="intro">
                    Savez-vous que la moitié des véhicules soumis à contres visites auraient pu l’éviter en effectuant l’une des 7 vérifications suivantes :
                </p>
                <p>
                    <ul>
                        <li>
                            <strong>L’éclairage et la signalisation :</strong> Vérifiez le bon fonctionnement de l’ensemble de vos feux :
                                <ul>
                                    <li>Croisement</li>
                                    <li>Route</li>
                                    <li>Position avant et arrière</li>
                                    <li>Clignotants</li>
                                    <li>Feux de détresses</li>
                                    <li>L’éclairage de la plaque arrière</li>
                                    <li>Klaxon</li>
                                </ul>
                            N’hésitez pas à changer une ampoule grillée ou à réparer un phare cassé avant de venir déposer votre véhicule.
                        </li>
                        <li>
                            <strong>Le Freinage :</strong> Faites remplacer vos plaquettes et vos disques de frein si ils sont usés. Si votre véhicule se déporte sur la gauche ou sur
                            la droite lors d’un freinage appuyé, c’est signe de freins en mauvais états. Dans ce cas faites vérifier vos freins par un professionnel. Vérifiez le
                            niveau du liquide de frein.
                        </li>
                        <li>
                            <strong>L’immatriculation :</strong> Les plaques d’immatriculation avant et arrière doivent être bien fixées et lisibles. Attention, seules les plaques
                            réglementaires sont accéptées.
                        </li>
                        <li>
                            <strong>La visibilité :</strong> Vérifiez votre pare-brise, si il a une fissure de plus de 30 cm de diamètre, il doit être changé. Vos
                            rétroviseurs intérieurs et extérieurs doivent être en bon état et bien fixés. Vérifier le bon fonctionnement de vos éssuie-glace.
                        </li>
                        <li>
                            <strong>Les pneumatique et l’échappement :</strong> Si vos pneus sont anormalement déformés (déchirure hernie, usure irrégulière) ou la bande de
                            roulement inférieure à 1,6 mm, alors n’hésiter pas à les changer. N’oubliez pas que les deux pneus d’un même essieu doivent être de même structure. Pour
                            votre pot d’échappement, il ne doit pas être percé.
                        </li>
                        <li>
                            <strong>L’habitacle :</strong> Vérifier que toutes les ceintures de sécurité fonctionnent bien et sont bien fixées. Assurez-vous que les sièges soient
                            correctement fixés. Si vous possédez un siège bébé, nous vous conseillons de l’enlever avant de passer le contrôle technique.
                        </li>
                        <li>
                            <strong>La structure et la carrosserie :</strong> Vérifier la bonne ouverture et fermeture de toutes les portes, du capot et du coffre. En cas de non
                            ouverture du capot moteur, le contrôle risque de ne pas être réalisé ce qui impliquerait une nouvelle visite complète. La carrosserie ne doit comporter
                            aucune partie saillante susceptible de blesser un tiers.
                        </li>
                    </ul>
                    Si vous avez des doutes sur l’état de votre véhicule, faites le voir par votre garagiste qui vous conseillera sur les réparations à effectuer pour être sûr qu’il
                    passera ce contrôle avec succès.
                </p>
